package category;

public class Category {
	private int mult;
	private double maxHr;
	
	/**
	 * @return the mult
	 */
	public int getMult() {
		return mult;
	}
	
	/**
	 * @param mult the mult to set
	 */
	public void setMult(int m) {
		this.mult = m;
	}
	
	/**
	 * @return the maxHr
	 */
	public double getMaxHr() {
		return maxHr;
	}
	
	/**
	 * @param maxHr the maxHr to set
	 */
	public void setMaxHr(double mhr) {
		this.maxHr = mhr;
	}

}
